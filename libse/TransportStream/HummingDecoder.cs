        public static int Decode2418(byte[] data, int offset)
        {
            return ((data[offset + 2] & 0x7F) << 11) |
                ((data[offset + 1] & 0x7F) << 4) |
                ((data[offset] & 0x70) >> 3) |
                ((data[offset] & 0x4) >> 2);
        }

        public static byte Decode84(byte[] data, int offset)
        {
            return Decode(data[offset]);
        }

        public static byte Decode(byte b1, byte b2)
        {
            return (byte)((_unhammingTable[b2] << 4) | (_unhammingTable[b1] & 0xF));
        }

        public static byte Decode(byte b)
        {
            return (byte)(_unhammingTable[b] & 0xF);
        }

